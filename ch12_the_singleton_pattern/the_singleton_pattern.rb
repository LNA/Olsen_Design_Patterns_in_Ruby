# The singleton in a single class that can have only one instance and provides 
# global access to that instance

# Helps to manage that single instance so we don't have to pass an object around a lot

# Eager instantiation is creating the singleton before we need it.
# Lazy instantiation is waiting until the instance is called to make the singleton.  This can be achieved
# by using a module.

# Alternatives to the classic singleton include using global variables, classes and modules as singletons.
